@Composable
fun green(): Color {
    return if (isDarkThemeApplied())
        darkGreen
    else
        lightGreen
}

@Composable
fun red(): Color {
    return if (isDarkThemeApplied())
        errorContainerDark
    else
        errorLight
}

@Composable
fun yellow(): Color {
    return if (isDarkThemeApplied())
        darkYellow
    else
        lightYellow
}

@Composable
fun violet(): Color {
    return if (isDarkThemeApplied())
        darkViolet
    else
        lightViolet
}

@Composable
fun BrownieTheme(
    darkTheme: Boolean = isDarkThemeApplied(),
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        darkTheme -> darkScheme
        else -> lightScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content
    )
}

@Composable
@NonRestartableComposable
private fun isDarkThemeApplied(): Boolean {
    return when (localUser.theme) {
        Light -> false
        Dark -> true
        else -> isSystemInDarkTheme()
    }
}

